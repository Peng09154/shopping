    {

        $inputs = $request->all();
        $id = $inputs['product_id'];
        $product = Product::find($id);
        $inputs['total'] = $inputs['quantity'] * $product->price;
        $product->stock = $product->stock - $inputs['quantity'];
        // dd($product);
        Order::create($inputs);

        $product->save();

        return redirect('success');
    }

